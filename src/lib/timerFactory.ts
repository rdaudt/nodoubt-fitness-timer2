export const randomTimerName = (random = Math.random): string => {
  const index = Math.floor(random() * TIMER_NAME_IDEAS.length);
  return TIMER_NAME_IDEAS[Math.min(index, TIMER_NAME_IDEAS.length - 1)];
};

export const newTimer = (): Timer => {
  const now = new Date().toISOString();
  return {
    id: crypto.randomUUID(),
    name: randomTimerName(),
    stationCount: 10,
    roundsPerStation: 3,
    workMinutes: 0,
    workSeconds: 30,
    restMinutes: 0,
    restSeconds: 15,
    stationTransitionMinutes: 0,
    stationTransitionSeconds: 30,
    startStationWorkManually: false,
    warmupEnabled: true,
    warmupMinutes: 5,
    warmupSeconds: 0,
    cooldownEnabled: true,
    cooldownMinutes: 5,
    cooldownSeconds: 0,
    createdAt: now,
    updatedAt: now,
  };
};
